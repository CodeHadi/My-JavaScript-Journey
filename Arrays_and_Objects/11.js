// array method

// push { koi cheez ak ko add krwni hai array ma }
let tite = ["hadi","huzaifa","hassan"];
tite.push("hammad");
console.log(tite);


// pop {koi cheez apko array may say niklwani hon}
let elite =  ["hadi","huzaifa","hassan","hammad"];
console.log(elite);
elite.pop();
console.log(elite);


// unshift method {jab ko koi cheez starting ma add krni hon}
elite.unshift("abdul")
console.log(elite);

// shift method  {jab ko koi cheez starting  ma remove krni hon}

elite.shift("abdul")
console.log(elite);

